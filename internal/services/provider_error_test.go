// TestParseThrottleHeaders covers the canonical header order +
// per-platform quirks. The worker relies on this for the
// retry-after the rate-limit handler stamps onto post_targets.
func TestParseThrottleHeaders(t *testing.T) {
	cases := []struct {
		name    string
		header  http.Header
		want    time.Duration
	}{
		{
			name:   "Retry-After delta-seconds (RFC 7231)",
			header: http.Header{"Retry-After": {"120"}},
			want:   120 * time.Second,
		},
		{
			name:   "X-RateLimit-Reset epoch (GitHub/Stripe/YouTube)",
			header: http.Header{"X-Ratelimit-Reset": {fmt.Sprintf("%d", time.Now().Add(90*time.Second).Unix())}},
			want:   90 * time.Second, // ±2s due to test wall clock
		},
		{
			name:   "X-Rate-Limit-Reset alt spelling",
			header: http.Header{"X-Rate-Limit-Reset": {"60"}},
			want:   60 * time.Second,
		},
		{
			name:   "x-rate-limit-reset lowercase (Twitter v2)",
			header: http.Header{"X-Rate-Limit-Reset": {"45"}}, // Canonical form (Go canonicalizes header keys)
			want:   45 * time.Second,
		},
		{
			name:   "missing all throttle headers",
			header: http.Header{},
			want:   0,
		},
		{
			name:   "Retry-After unparseable",
			header: http.Header{"Retry-After": {"not-a-number"}},
			want:   0,
		},
		{
			name: "prefers Retry-After over X-RateLimit-Reset (canonical first)",
			header: http.Header{
				"Retry-After":       {"30"},
				"X-Ratelimit-Reset": {"999999999"}, // 11+ years in the future
			},
			want: 30 * time.Second,
		},
	}
	for _, c := range cases {
		t.Run(c.name, func(t *testing.T) {
			got := ParseThrottleHeaders(c.header)
			if c.want == 0 {
				if got != 0 {
					t.Errorf("want 0, got %v", got)
				}
				return
			}
			// Allow ±2s tolerance for epoch-based tests (test wall
			// clock vs the value computed at test setup).
			diff := got - c.want
			if diff < 0 {
				diff = -diff
			}
			if diff > 2*time.Second {
				t.Errorf("want ~%v, got %v (diff %v)", c.want, got, diff)
			}
		})
	}
}

