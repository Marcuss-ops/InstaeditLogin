// TestMapHTTPStatus covers the canonical HTTP status → taxonomy
// code mapping for 4xx and 5xx. The function is documented as
// not-for-2xx/3xx; those cases are NOT in the table (a regression
// returning "" for 200 would silently slip through if they were,
// because there's no positive assertion for them). The taxonomy
// is the contract that the worker / API layer act on; a regression
// here changes the retry behavior of every provider.
func TestMapHTTPStatus(t *testing.T) {
	cases := []struct {
		status int
		want   ProviderErrorCode
	}{
		{http.StatusBadRequest, ErrorCodeValidationError},
		{401, ErrorCodeAuthenticationError},
		{http.StatusPaymentRequired, ErrorCodeQuotaExceeded},
		{http.StatusForbidden, ErrorCodePermissionMissing},
		{http.StatusNotFound, ErrorCodeContentRejected},
		{http.StatusGone, ErrorCodeContentRejected},
		{http.StatusTooManyRequests, ErrorCodeRateLimited},
		{418, ErrorCodeValidationError},    // 4xx catch-all
		{451, ErrorCodeValidationError},    // 4xx catch-all
		{http.StatusInternalServerError, ErrorCodeProviderUnavailable},
		{http.StatusBadGateway, ErrorCodeProviderUnavailable},
		{http.StatusServiceUnavailable, ErrorCodeProviderUnavailable},
		{599, ErrorCodeProviderUnavailable},
		{0, ErrorCodeInternalError}, // 0 = not-applicable defensive default
	}
	for _, c := range cases {
		got := MapHTTPStatus(c.status)
		if got != c.want {
			t.Errorf("MapHTTPStatus(%d) = %q, want %q", c.status, got, c.want)
		}
	}
}

