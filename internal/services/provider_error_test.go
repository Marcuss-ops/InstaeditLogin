// TestProviderError_ImplementsError pins the error interface
// contract. The compile-time check on `var _ error = (*ProviderError)(nil)`
// would catch a regression; this test also documents it.
func TestProviderError_ImplementsError(t *testing.T) {
	var _ error = (*ProviderError)(nil)
	e := &ProviderError{Platform: "tiktok", Code: ErrorCodeRateLimited}
	if e.Error() == "" {
		t.Fatal("Error() returned empty string")
	}
}

// TestProviderError_ErrorFormat verifies the canonical Error()
// format: "{platform}: {code} ({safe_message})". A regression that
// changes the format would break log scrapers and the API layer's
// user-facing error messages.
func TestProviderError_ErrorFormat(t *testing.T) {
	e := &ProviderError{
		Platform:    "tiktok",
		Code:        ErrorCodeRateLimited,
		SafeMessage: "tiktok operation failed (status 429): rate limited",
	}
	want := "tiktok: rate_limited (tiktok operation failed (status 429): rate limited)"
	if got := e.Error(); got != want {
		t.Errorf("Error() = %q, want %q", got, want)
	}
}

