// TestNewProviderError_Adapter_401 covers the canonical "external
// 401 → authentication_error" mapping. The user spec required
// "at least 2-3 external error forms → taxonomy" — this is one
// of the three.
func TestNewProviderError_Adapter_401(t *testing.T) {
	ts := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.WriteHeader(http.StatusUnauthorized)
		w.Write([]byte(`{"error":"invalid_token"}`))
	}))
	defer ts.Close()
	resp, err := http.Get(ts.URL)
	if err != nil {
		t.Fatalf("setup: %v", err)
	}
	defer resp.Body.Close()
	bodyBuf := make([]byte, 256)
	n, _ := resp.Body.Read(bodyBuf)

	pe := NewProviderError("tiktok", resp.StatusCode, string(bodyBuf[:n]), resp.Header, nil)
	if pe.Code != ErrorCodeAuthenticationError {
		t.Errorf("Code: want %q, got %q", ErrorCodeAuthenticationError, pe.Code)
	}
	if pe.StatusCode != http.StatusUnauthorized {
		t.Errorf("StatusCode: want %d, got %d", http.StatusUnauthorized, pe.StatusCode)
	}
	// SafeMessage must not contain the body substring.
	if strings.Contains(pe.SafeMessage, "invalid_token") {
		t.Errorf("SafeMessage leaked body: %q", pe.SafeMessage)
	}
	// 401 is NOT retryable (auth required).
	if pe.Retryable {
		t.Errorf("401 should NOT be retryable; got Retryable=true")
	}
}

