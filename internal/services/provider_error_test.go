// TestNewProviderError_NewRateLimitErrorConstructor: the
// convenience constructor returns a fully-populated ProviderError
// without needing an HTTP response. The status/SafeMessage/
// ProviderCode/RequestID defaults are pinned: callers downstream
// (the API layer that builds a JSON error response) depend on
// these NOT being zero values.
func TestNewProviderError_NewRateLimitErrorConstructor(t *testing.T) {
	pe := NewRateLimitError("linkedin", 90*time.Second)
	if pe.Code != ErrorCodeRateLimited {
		t.Errorf("Code: want %q, got %q", ErrorCodeRateLimited, pe.Code)
	}
	if pe.RetryAfter != 90*time.Second {
		t.Errorf("RetryAfter: want 90s, got %v", pe.RetryAfter)
	}
	if !pe.Retryable {
		t.Errorf("rate_limited should be retryable; got Retryable=false")
	}
	if pe.StatusCode != http.StatusTooManyRequests {
		t.Errorf("StatusCode: want 429 (the constructor must pin this), got %d", pe.StatusCode)
	}
	if pe.Platform != "linkedin" {
		t.Errorf("Platform: want %q, got %q", "linkedin", pe.Platform)
	}
	if pe.SafeMessage == "" {
		t.Errorf("SafeMessage: want non-empty, got empty")
	}
	if !strings.Contains(pe.SafeMessage, "linkedin") {
		t.Errorf("SafeMessage: should contain platform name %q; got %q", "linkedin", pe.SafeMessage)
	}
	if !strings.Contains(pe.SafeMessage, "rate limited") {
		t.Errorf("SafeMessage: should contain category 'rate limited'; got %q", pe.SafeMessage)
	}
	if pe.ProviderCode != "" {
		t.Errorf("ProviderCode: want empty (no upstream body parsed), got %q", pe.ProviderCode)
	}
	if pe.RequestID != "" {
		t.Errorf("RequestID: want empty (no upstream headers parsed), got %q", pe.RequestID)
	}
	if pe.Cause != nil {
		t.Errorf("Cause: want nil (constructor never sets it), got %v", pe.Cause)
	}
}

