// TestProviderError_UnwrapsCause verifies the error chain:
// errors.Is(pe, cause) must return true so callers can detect
// transport-layer causes (context.Canceled, io.EOF, etc.) through
// the ProviderError wrapping.
func TestProviderError_UnwrapsCause(t *testing.T) {
	cause := errors.New("underlying network timeout")
	pe := &ProviderError{Platform: "tiktok", Code: ErrorCodeProviderUnavailable, Cause: cause}
	if !errors.Is(pe, cause) {
		t.Errorf("errors.Is should find Cause through the wrap; err = %v", pe)
	}
}

// TestProviderError_SafeMessageDoesNotLeakBody is the CRITICAL
// security test. The raw provider body may contain user PII,
// internal tokens, or other sensitive data. The SafeMessage must
// NEVER contain any of the body substring. A regression here would
// be a privacy incident. Also covers the body → Cause autopreserve
// branch (when caller passes nil cause).
func TestProviderError_SafeMessageDoesNotLeakBody(t *testing.T) {
	body := `{"error": "secret_pii_account_12345: leaked internal token abcdef"}`
	err := NewProviderError("twitter", 400, body, http.Header{}, nil)
	// 1. Error() must not contain any of the body substring.
	if strings.Contains(err.Error(), "secret_pii_account_12345") {
		t.Fatalf("Error() leaked body substring: %s", err.Error())
	}
	if strings.Contains(err.Error(), "leaked internal token") {
		t.Fatalf("Error() leaked body substring: %s", err.Error())
	}
	if strings.Contains(err.SafeMessage, "secret_pii_account_12345") {
		t.Fatalf("SafeMessage leaked body substring: %s", err.SafeMessage)
	}
	// 2. Cause must contain the body (body autopreserve when caller
	// passes nil). The truncateForLog helper caps at 256 chars so
	// huge bodies don't bloat the cause chain.
	if err.Cause == nil {
		t.Fatal("Cause should preserve the raw error for debug; got nil")
	}
	if !strings.Contains(err.Cause.Error(), "secret_pii_account_12345") {
		t.Errorf("Cause should contain body substring for debug logs; got %q", err.Cause.Error())
	}
}

// TestProviderError_CallerCausePreserved covers the OTHER arm of
// the cause-preservation branch: when the caller passes a non-nil
// cause, the caller's cause must be preserved VERBATIM and the
// body is NOT wrapped into it. Otherwise a caller attaching their
// own rich context (e.g. "twitter 2/tweets: ...") would have it
// silently overridden by an autogenerated "provider body (truncated): ...".
func TestProviderError_CallerCausePreserved(t *testing.T) {
	body := `{"error": "ignore_me_for_cause"}`
	callerCause := errors.New("twitter 2/tweets ctx: caller-attach")
	err := NewProviderError("twitter", 400, body, http.Header{}, callerCause)
	if err.Cause != callerCause {
		t.Fatalf("Cause: want caller-supplied %v, got %v", callerCause, err.Cause)
	}
	if strings.Contains(err.Cause.Error(), "ignore_me_for_cause") {
		t.Errorf("Cause should NOT contain autogenerated body wrap; got %q", err.Cause.Error())
	}
}

// TestProviderError_DefaultsFilled verifies the constructor fills
// all expected fields: Code, Platform, SafeMessage, StatusCode, and
// (where applicable) Retryable / RetryAfter. A regression that
// drops a field would surface as a missing key in the API response.
func TestProviderError_DefaultsFilled(t *testing.T) {
	h := http.Header{}
	h.Set("Retry-After", "90")
	err := NewProviderError("linkedin", 429, `{"serviceErrorCode":429}`, h, nil)
	if err.Code != ErrorCodeRateLimited {
		t.Errorf("Code: want %q, got %q", ErrorCodeRateLimited, err.Code)
	}
	if !err.Retryable {
		t.Errorf("Retryable: want true for rate_limited, got false")
	}
	if err.RetryAfter != 90*time.Second {
		t.Errorf("RetryAfter: want 90s, got %v", err.RetryAfter)
	}
	if err.Platform != "linkedin" {
		t.Errorf("Platform: want %q, got %q", "linkedin", err.Platform)
	}
	if err.StatusCode != 429 {
		t.Errorf("StatusCode: want 429, got %d", err.StatusCode)
	}
	if err.ProviderCode != "429" {
		t.Errorf("ProviderCode: want %q, got %q", "429", err.ProviderCode)
	}
	if err.SafeMessage == "" {
		t.Errorf("SafeMessage: want non-empty, got empty")
	}
}

// TestProviderError_NilSafe verifies that calling Error() on a
// nil *ProviderError doesn't panic. Defensive — callers sometimes
// do `if pe := ...; pe != nil` and a panic here would be a crash.
func TestProviderError_NilSafe(t *testing.T) {
	var pe *ProviderError
	defer func() {
		if r := recover(); r != nil {
			t.Fatalf("Error() panicked on nil receiver: %v", r)
		}
	}()
	got := pe.Error()
	if got != "<nil ProviderError>" {
		t.Errorf("Error() on nil: want %q, got %q", "<nil ProviderError>", got)
	}
}

